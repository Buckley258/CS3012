
public class TreeNode {

	public int val;
	public TreeNode left = null;
	public TreeNode right = null;

	public TreeNode(int val) {
		this.val = val;
	}

	public int getVal() {
		return val;
	}

	public TreeNode getLeftNode() {
		return left;
	}

	public TreeNode getRightNode() {
		return right;
	}

}
